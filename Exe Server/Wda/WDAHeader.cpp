#include "stdafx.h"
#include "WDAHeader.h"
#include "WDAFile.h"

#define TAG_VALUE 0x0CA7

/******************************************************************************/
WDAHeader::WDAHeader() : m_ReadOnly( false )
/******************************************************************************/
{
}
/******************************************************************************/
WDAHeader::~WDAHeader()
/******************************************************************************/
{
}
/******************************************************************************/
bool WDAHeader::CreateFrom( WDAFile &wdaFile )
/******************************************************************************/
{
    WORD tag;

    wdaFile.Read( tag );
    wdaFile.Read( m_Version );
    wdaFile.Read( m_ReadOnly );

    if( tag != TAG_VALUE || !IsVersionOK() )
	{
        return false;
    }
    return true;
}
/******************************************************************************/
bool WDAHeader::IsVersionOK()
{
    return m_Version == 1;
}